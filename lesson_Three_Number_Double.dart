void main(List<String> args) {
  num a = 321;
  double b = 252.25;
  int c = 69;

  var abc = 35;

  /*
  num is for full number
  doble is for decimal number 
  */

  print(a);
  print(b);
  print(c);
  print(abc);

  //null safety
  int number;
  number = 31;
  print(number);

  //null safety 
  double? n1 = null;
  print(n1);

  int hexadecimalNumber = 0xAABBCC;
  print(hexadecimalNumber);
}
